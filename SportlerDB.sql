CREATE TABLE TeamsportArt(SA_Id NUMBER NOT NULL PRIMARY KEY, T_Id NUMBER NOT NULL);
CREATE TABLE EinzelsportArt(SA_Id NUMBER NOT NULL PRIMARY KEY);
