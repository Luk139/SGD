    Anzahl_Teams NUMBER NOT NULL);
CREATE TABLE Sportler_Team(S_Id NUMBER NOT NULL PRIMARY KEY, T_Id NUMBER NOT NULL, 
    Beitrittsdatum DATE);
CREATE TABLE Wettkampf(WK_Id NUMBER NOT NULL PRIMARY KEY, WK_Name varchar2(50) NOT NULL, 
    W_Id NUMBER NOT NULL);
CREATE TABLE Einzelwettkampf(WK_Id NUMBER NOT NULL PRIMARY KEY, S_Id_1 NUMBER NOT NULL, 
    S_Id_2 NUMBER NOT NULL, Datum DATE, Sieger NUMBER NOT NULL);
CREATE TABLE Teamwettkampf(WK_Id NUmber NOT NULL PRIMARY KEY, T_Id_1 NUMBER NOT NULL, 
    T_Id_2 NUMBER NOT NULL, DATUM DATE, Sieger NUMBER NOT NULL);
CREATE TABLE Team(T_Id NUMBER NOT NULL PRIMARY KEY,T_Name varchar2(50) NOT NULL, 
    SP_Id NUMBER NOT NULL, L_Id NUMBER NOT NULL, Gründungsjahr NUMBER NOT NULL,
    Anzahl_Sportler NUMBER NOT NULL);
