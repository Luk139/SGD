CREATE TABLE Sportler(S_Id NUMBER NOT NULL PRIMARY KEY, S_Vorname varchar2(30), 
    S_Nachname varchar2(30), S_Geburtsdatum DATE, L_Id NUMBER NOT NULL, 
    SP_Id NUMBER NOT NULL, S_Siege NUMBER NOT NULL, S_Niederlagen NUMBER NOT NULL, 
    S_GesamtWettkämpfe NUMBER NOT NULL);
CREATE TABLE Land(L_Id NUMBER NOT NULL PRIMARY KEY, L_Name varchar2(30) NOT NULL);
